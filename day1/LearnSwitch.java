package week1.day1;

public class LearnSwitch {

	public static void main(String[] args) {
		String browsertype = "Chrome";
	
		switch (browsertype) {
		case "Edge":System.out.println("Open chrome");break;
		case "Firefox":System.out.println("Open Firefox");break;
		case "Chrome": System.out.println("Open chrome"); break;
		case "Opera":  System.out.println("Open opera");break;

			}
	
	
	
	}

}
